class Solution
{
    public:
    //Function to check whether a Binary Tree is BST or not.
    bool testnode(Node *current, int Min, int Max)
    {
        if(current==NULL)
        {
            return true;
            
        }
        
        if(current->data<Min || current->data>Max)
        return false;
        
        return testnode(current->left,Min,min(Max ,current->data)) && testnode(current->right,max(Min, current->data),Max);
    }
    
    bool isBST(Node* root) 
    {
        // Your code here
        testnode(root,INT_MIN, INT_MAX);
    }
};




